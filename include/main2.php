                <h2>Monitori i elektrokardiogramit (EKG)</h2>
                <p>EKG zbulon ritmin e zemrës dhe forcën e impulseve elektrike brenda zemrës. &#199do devijim tregon një
                     problem me zemrën. Për më tepër, testi i EKG-së ndihmon në vlerësimin e shkakut të dhimbjes së 
                     gjoksit, rrahjeve të parregullta të zemrës dhe funksionit të implantuar të stimuluesit kardiak.
                      Përveç kësaj, ai mund të udhëzojë kardiologët në diagnostikimin e hipertensionit, hipotensionit,
                       aritmive dhe dëmtimit të miokardit.</p>
            </article>
           <article id="echo">
                <img src="images/echo.jpg">
                <h2>Makinat e ekokardiogramit</h2>
                <p>Një makinë ekokardiografie është një pajisje jo-invazive që përdor valët e zërit për të kapur 
                    zemrën dhe enët e brendshme të gjakut. Ai përfshin një transduktor të vendosur në gjoks për 
                    të dërguar valë zanore në zemër. Pra, kur zemra i largon valët, dhënësit i zgjedhin ato. Pas 
                    kësaj, makina përdor valët e zërit për të krijuar një pamje të zemrës. Imazhi që rezulton ndihmon 
                    mjekun të vlerësojë funksionin e zemrës.</p>
            </article>
           <article id="apb">
                <img src="images/abp.jpg">
                <h2>Monitorët ambulatorë të presionit të gjakut (ABP)</h2>
                <p>Një monitor ambulator i presionit të gjakut (ABP) është një version mini i monitorit të rregullt
                     dixhital të presionit të gjakut (BP). Monitoruesi ABP funksionon tamam si monitorët tradicionalë
                      duke lexuar presionin e gjakut dhe rrahjet e zemrës së pacientit, por me një frekuencë të ndryshme
                       monitorimi. Ai vëzhgon rregullisht presionin e gjakut të pacientit gjatë 24 orëve.</p>
            </article>
            <article id="vitalSigns">
                <img src="images/vitalsigns.jpg">
                <h2>Monitoruesit e shenjave vitale</h2>
                <p>Monitori i shenjave vitale vlerëson statusin fiziologjik të një personi. Parametrat bazë të regjistruar
                     me monitorët përfshijnë temperaturën e trupit, shkallën e frymëmarrjes, pulsin dhe presionin e 
                     gjakut. Megjithatë, disa modele përfshijnë një kontroll SpO<sub>2</sub>. Gjithashtu, ka standarde 
                     për çdo parametër, kështu që çdo devijim tregon një gjendje jonormale shëndetësore.</p>
            </article>
            <article id="pulseOximeter">
                <img src="images/pulseoximeter.gif">
                <h2>Oksimetrat e pulsit</h2>
                <p>Një oksimetër i pulsit është një pajisje elektronike që përdoret për të matur nivelin e ngopjes së 
                    oksigjenit në gjak (SpO<sub>2</sub>). Vlera e SpO<sub>2</sub> tregon sasinë e oksigjenit në qelizat e kuqe të gjakut. 
                    Gjithashtu, oksimetri mund të masë pulsin e një personi. Pajisja vendoset në majë të gishtit dhe
                     përdor dritë për të matur SpO<sub>2</sub>dhe pulsin e një personi. Pulsoksimetri është veçanërisht 
                     i dobishëm për pacientët me probleme të mushkërive.</p>
